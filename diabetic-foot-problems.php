    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      Common Diabetic Foot Problems You Should Know About
    </h2>

    <ol class="list-decimal pl-6 mb-8 space-y-4">
      <li>
        <strong>Neuropathy:</strong> High blood sugar levels can damage the nerves in the feet, causing numbness, tingling, and loss of sensation. This makes it difficult to notice cuts, blisters, or injuries, increasing infection risk.
      </li>
      <li>
        <strong>Poor Circulation:</strong> Diabetes can reduce blood flow to the feet, slowing wound healing and increasing the risk of infections.
      </li>
      <li>
        <strong>Foot Ulcers:</strong> Open sores or wounds that develop due to nerve damage and poor circulation. If untreated, ulcers can become infected and lead to serious complications.
      </li>
      <li>
        <strong>Charcot Foot:</strong> A rare but serious condition affecting the bones and joints of the foot, leading to deformity, chronic pain, and disability.
      </li>
      <li>
        <strong>Calluses and Corns:</strong> Increased pressure on the feet due to nerve damage can lead to thickened skin, corns, and calluses that may break down into ulcers.
      </li>
      <li>
        <strong>Fungal Infections:</strong> Diabetes increases the risk of fungal infections such as athleteâ€™s foot, which can worsen if not treated promptly.
      </li>
      <li>
        <strong>Amputations:</strong> In severe cases, diabetic foot problems may require amputation. According to the American Diabetes Association, nearly 60% of non-traumatic lower-limb amputations occur in people with diabetes.
      </li>
    </ol>

    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      Why Foot Care Is Crucial for People with Diabetes
    </h2>

    <p class="mb-6">
      Because nerve damage can reduce pain sensation, small injuries may go unnoticed until they become serious. Poor circulation further delays healing, increasing the risk of infection and complications.
    </p>

    <p class="mb-8">
      Regular foot care and early medical attention can prevent most diabetic foot complications.
    </p>

    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      How to Prevent Diabetic Foot Problems
    </h2>

    <ul class="list-disc pl-6 mb-8 space-y-1">
      <li>Inspect your feet daily for cuts, blisters, redness, or swelling</li>
      <li>Wear comfortable, well-fitting footwear</li>
      <li>Keep feet clean and dry</li>
      <li>Trim nails carefully to avoid injury</li>
      <li>Control blood sugar levels consistently</li>
      <li>Schedule regular foot check-ups with a healthcare provider</li>
    </ul>

    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      Conclusion
    </h2>

    <p>
      Diabetic foot problems are common but largely preventable with proper care and early intervention. By maintaining good blood sugar control, practicing daily foot care, and seeking medical advice at the first sign of trouble, individuals with diabetes can significantly reduce the risk of serious foot complications.
    </p>

