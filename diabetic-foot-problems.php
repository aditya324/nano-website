    <p class="mb-6">
      Diabetic foot problems refer to a range of medical conditions that affect the feet of people with diabetes. Diabetes can lead to nerve damage and poor blood circulation, which can cause a variety of foot-related complications.
    </p>

    <p class="mb-8">
      These problems can range from minor cuts and blisters to severe infections and even amputations. Understanding these conditions early is essential for preventing serious outcomes.
