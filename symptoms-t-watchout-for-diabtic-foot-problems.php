    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      Common Symptoms of Diabetic Foot Problems
    </h2>

    <ol class="list-decimal pl-6 mb-8 space-y-4">
      <li>
        <strong>Numbness or Tingling:</strong> High blood sugar levels can damage nerves in the feet, causing numbness, tingling, or loss of sensation. This may prevent you from noticing injuries.
      </li>
      <li>
        <strong>Changes in Skin Color or Temperature:</strong> Reduced blood flow can lead to redness, bluish or purplish discoloration, or unusually cold feet.
      </li>
      <li>
        <strong>Swelling:</strong> Fluid retention and poor circulation can cause swelling in the feet and ankles, often accompanied by discomfort.
      </li>
      <li>
        <strong>Foot Pain:</strong> Pain may result from nerve damage, reduced circulation, infections, or unnoticed injuries.
      </li>
      <li>
        <strong>Wounds or Sores:</strong> Cuts, blisters, or ulcers that heal slowly or not at all increase the risk of infection.
      </li>
      <li>
        <strong>Calluses or Corns:</strong> Increased pressure on certain areas of the foot can cause thickened skin that may break down into ulcers.
      </li>
      <li>
        <strong>Fungal Infections:</strong> Conditions like athlete’s foot can cause itching, redness, cracking, and peeling of the skin.
      </li>
    </ol>

    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      When to See a Healthcare Provider
    </h2>

    <p class="mb-6">
      If you notice any of the symptoms mentioned above, it’s important to consult a healthcare provider as soon as possible. Early diagnosis and timely treatment can prevent serious complications such as infections, ulcers, and amputations.
    </p>

    <p class="mb-8">
      Even if you don’t have symptoms, regular foot check-ups are essential for people with diabetes to detect problems early and maintain foot health.
    </p>

    <h2 class="text-2xl font-semibold text-red-700 mb-4">
      Conclusion
    </h2>

    <p>
      Diabetic foot problems can often be prevented with awareness, daily foot care, and early medical intervention. Monitoring your feet regularly and seeking professional care at the first sign of trouble can protect your mobility and overall health.
    </p>

