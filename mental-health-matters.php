            <li>Sleeplessness or disturbed sleep</li>
            <li>Mood swings</li>
            <li>Low motivation, especially for work or daily tasks</li>
            <li>Emotional breakdowns or crying spells</li>
            <li>Difficulty focusing or concentrating</li>
            <li>Feeling “empty”, numb, or disconnected</li>
