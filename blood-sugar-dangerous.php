        <h2 class="text-2xl font-semibold text-red-700 mb-4">
            What Are Blood Sugar Levels?
        </h2>

        <p class="mb-6">
            Before diving into the dangerous levels, let's establish what blood sugar levels entail. Blood sugar, or glucose, is the primary source of energy for our bodies, derived from the foods we consume.
        </p>

        <p class="mb-6">
            Glucose levels are measured in milligrams per deciliter (mg/dL) of blood. Normal blood sugar levels typically range between <strong>70 to 100 mg/dL</strong> when fasting, and below <strong>140 mg/dL</strong> two hours after eating.
        </p>

        <h2 class="text-2xl font-semibold text-red-700 mb-4">
            Understanding Dangerous Blood Sugar Levels
        </h2>

        <ol class="list-decimal pl-6 mb-8 space-y-4">
            <li>
                <strong>Hypoglycemia:</strong> This occurs when blood sugar levels drop below <strong>70 mg/dL</strong>. It can result from prolonged fasting, excessive insulin dosage, or certain medical conditions.
                <br />
                <span class="block mt-2">
                    Symptoms include trembling, sweating, dizziness, confusion, and in severe cases, loss of consciousness.
                </span>
            </li>
            <li>
                <strong>Hyperglycemia:</strong> On the other end of the spectrum, hyperglycemia refers to high blood sugar levels, usually above <strong>180 mg/dL</strong>.
                <br />
                <span class="block mt-2">
                    Causes include insufficient insulin production, excessive carbohydrate intake, stress, illness, or certain medications. Symptoms may include increased thirst, frequent urination, fatigue, and blurred vision.
                </span>
            </li>
        </ol>

