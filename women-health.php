        <h2 class="text-2xl font-semibold text-red-700 mb-4">
            10 Practical Wellness Tips for Working Women in 2025
        </h2>

        <p class="mb-6">
            These are easy, doable steps designed specially for Bengaluru’s fast-paced lifestyle.
        </p>

        <ol class="list-decimal pl-6 mb-8 space-y-4">
            <li>
                <strong>Eat Every 3–4 Hours</strong><br />
                Skipping meals leads to acidity, weight gain, and hormonal imbalance. Carry fruits, nuts, or healthy snacks to office so you don’t rely on junk food or long gaps between meals.
            </li>
            <li>
                <strong>20-Minute Daily Movement</strong><br />
                You don’t need a gym membership to stay active. Walking in your apartment, taking the stairs, or doing simple stretches during work breaks is enough.
            </li>
            <li>
                <strong>Fix Your Sleep Window</strong><br />
                Aim for a fixed sleep window like 11:00 PM – 6:00 AM or a steady 7–8 hours of sleep.
            </li>
            <li>
                <strong>Hydration Habit</strong><br />
                Keep a 1-litre bottle on your desk and refill it through the day.
            </li>
            <li>
                <strong>Monthly “Me-Time” Appointment</strong><br />
                Schedule one non-negotiable “me-time” appointment every month.
            </li>
            <li>
                <strong>Don’t Ignore Period Irregularities</strong><br />
                If your cycle is delayed, painful, very light, or very heavy, consult a gynecologist early.
            </li>
            <li>
                <strong>Do Annual Basic Health Tests</strong><br />
                CBC, thyroid profile, PCOS screening, Vitamin D & B12, lipid profile, and Pap smear (after 25).
            </li>
            <li>
                <strong>Protect Your Back & Neck</strong><br />
                Use ergonomic seating and stretch every 2 hours.
            </li>
            <li>
                <strong>Mental Health Check</strong><br />
                Speak to someone if you experience anxiety, sleep issues, or emotional distress.
            </li>
            <li>
                <strong>Learn to Say “No”</strong><br />
                Setting boundaries protects your mental peace and hormonal balance.
            </li>
        </ol>

